-module(crdt).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

-export([add/1, list/0]).

%%====================================================================
%% API
%%====================================================================

start(normal, []) -> crdt_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) -> crdt_sup:stop().

add(Key) -> crdt_server:add(Key).

list() -> crdt_server:list().

